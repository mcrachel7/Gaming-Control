    void ordenar_datos(){
        clase1 temp=new clase1();
        for(int i=0;i<arr.size();i++){
            for(int x=1;x<arr.size()-1;x++){
                if(arr.get(x-1).getSet()<arr.get(x).getSet()){
                    temp.setEquipo(arr.get(x-1).getEquipo());
                    temp.setVolante(arr.get(x-1).getVolante());
                    temp.setReferi(arr.get(x-1).getReferi());
                    temp.setSet(arr.get(x-1).getSet());
                    temp.setJornada(arr.get(x-1).getJornada());
                    temp.setMosca(arr.get(x-1).isMosca());
                    
                    arr.get(x-1).setEquipo(arr.get(x).getEquipo());
                    arr.get(x-1).setVolante(arr.get(x).getVolante());
                    arr.get(x-1).setReferi(arr.get(x).getReferi());
                    arr.get(x-1).setSet(arr.get(x).getSet());
                    arr.get(x-1).setJornada(arr.get(x).getJornada());
                    arr.get(x-1).setMosca(arr.get(x).isMosca());
                    
                    arr.get(x).setEquipo(temp.getEquipo());
                    arr.get(x).setVolante(temp.getVolante());
                    arr.get(x).setReferi(temp.getReferi());
                    arr.get(x).setSet(temp.getSet());
                    arr.get(x).setJornada(temp.getJornada());
                    arr.get(x).setMosca(temp.isMosca());                                                                
                }             
            }
        }
      }
    
    
    
    void presentar(){
       model.setNumRows(0);
      
        for(int i=0;i<3;i++){
                model.addRow(new Object[]{
                    arr.get(i).getEquipo(),
                    arr.get(i).getVolante(),
                    arr.get(i).getReferi(),
                    arr.get(i).getSet(),
                    arr.get(i).getJornada(),
                    arr.get(i).isMosca()                
                });
        }
     }
    
